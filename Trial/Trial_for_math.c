#include <stdio.h>
#include <math.h>
int main(){
    printf("The ceil value of 3.6 is %.2f\n", ceil(3.6));
    printf("The ceil value of 3.4 is %f\n", ceil(3.4));
    printf("The floor value of 3.6 is %f\n", floor(3.6) );
    printf("The floor value of 3.6 is %f\n", floor(3.4));
    printf("The square root value of 3.6 is %f\n", sqrt(16) );
    printf("The square root value of 3.6 is %f\n", sqrt(7) );
    printf("The power root value of 3.6 is %f\n", pow(2,4));
    printf("The power root value of 3.6 is %f\n", pow(2,4));
    printf("The absolute value of 3.6 is %d\n",abs(-12) );

    return 0;
}